      <b-collapse id="nav-collapse" is-nav>
        <b-navbar-nav>
          <b-nav-item-dropdown text="Product" left>
            <b-dropdown-item
              v-for="category in categories"
                :key="category.uid"
                active-class="active"
                :to="'/c/' + category.url_path + category.url_suffix"
            >{{ category.name }}
            </b-dropdown-item>
          </b-nav-item-dropdown>
        </b-navbar-nav>

        <!-- Right aligned nav items -->
        <b-navbar-nav class="ml-auto">
          <b-nav-form>
